    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (nuevoLibro.getCodigo().isEmpty() || nuevoLibro.getTitulo().isEmpty() ||
                nuevoLibro.getAutor().isEmpty()) {
            JOptionPane.showMessageDialog(this, "No se puede dejar ningún campo en blanco.");
        } else if (nuevoLibro.getCodigo().length() != 5 ) {
            JOptionPane.showMessageDialog(this, "El código tiene que ser de 5 dígitos.");
        } else if (nuevoLibro.getGenero().equals("---Escoge un género---") ||
                nuevoLibro.getGenero().equals("")) {
            JOptionPane.showMessageDialog(this, "Tienes que escoger un género.");
        } else if(nuevoLibro.getNumPaginas() < 1) {
            JOptionPane.showMessageDialog(this, "El número de páginas no puede ser menor que 1.");
        } else if (nuevoLibro.getCoste() < 1){
            JOptionPane.showMessageDialog(this, "El coste no puede ser menor que 1€");
        } else if (Biblioteca.misLibros.existe(nuevoLibro)) {
            JOptionPane.showMessageDialog(this, "Ya existe un libro con este código.");
        } else {
            Biblioteca.misLibros.altaLibro(nuevoLibro);
            Biblioteca.miFichero.grabar(Biblioteca.misLibros);
            JOptionPane.showMessageDialog(this, "Libro dado de alta!");
            dispose();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

